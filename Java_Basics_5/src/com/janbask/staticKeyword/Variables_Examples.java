package com.janbask.staticKeyword;

public class Variables_Examples {

	int age=20;//non static variables
	
	String name="kevin";//non static variables
	
	
	public static void main(String[] args) {
		
		
		Variables_Examples v = new Variables_Examples();
		System.out.println(v.age);//object.variable
		System.out.println(v.name);//object.variable
		
		
		
		

	}

}
